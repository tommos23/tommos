<h1>No Pussy Lent</h1>
In Lent, it's traditional to give up (or 'fast from') something(s) that we do a lot of and that we find <b>pleasure</b> in. This giving up or fasting is done:
<ul>
<li>as a discipline for learning self-control, to free our minds from the chase after material things, to tell ourselves <i>'no'</i> and make it <i>stick...y</i></li>

<li>to identify with Christ's sufferings (bet he did), and remember what the true <i>'pleasures'</i> are for followers of Christ;</li>

<li>as an <i>'act'</i> of sorrow over our wrongdoings and our state of <i>sin</i>.</li>
</ul>

It may at times be about forensic <i>'guilt'</i> (as in TV's CSI or Law and Order, the <b>'I did it'</b> kind of guilt), but it's not about the psychological kind of guilt (where God is pictured like a nagging mother (ohhhhhhhhhhh your mum), saying just the right word to make you feel sorry for yourself). In fact, it prepares you for Easter, in which a risen Christ leaves you no cause (or even room) for psychological guilt.<br><br>


Sometimes we don't notice how certain things we do have gained power over us and <i>'dictate'</i> our actions. In Lent fasts, we discover these things and give them up so that God can be in charge. Franciscans use the term 'detachment': the less that 'stuff' preoccupies your life, the more room there is for God, as well as for yourself and for other people.<br>
